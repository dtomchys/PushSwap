#include <string.h>

char	*ft_strcat(char *dest, const char *src)
{
	size_t i;
	size_t j;

	i = 0;
	while (dest[i])
		i++;
	j = 0;
	while (src[j])
	{
		dest[i++] = src[j++];
		if (src[j] == '\0')
			dest[i] = '\0';
	}
	return (dest);
}
